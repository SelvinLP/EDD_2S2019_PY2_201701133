    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String usu=jTextField1.getText();
        String pas=jPasswordField1.getText();
        if((usu.equals("Admin")||usu.equals("admin")||usu.equals("ADMIN"))&&(pas.equals("Admin")||pas.equals("admin")||pas.equals("ADMIN"))){
            Formulario_Admin FormAdmin=new Formulario_Admin();
            FormAdmin.show();
            this.dispose();
             //bitacora
            Calendar calendario = new GregorianCalendar();
            int hora =calendario.get(Calendar.HOUR_OF_DAY);
            int minutos = calendario.get(Calendar.MINUTE);
            int segundos = calendario.get(Calendar.SECOND);
            String tiempo=hora + ":" + minutos + ":" + segundos;
            EDDPY2_201701133.Bitacora.Insertar(tiempo, "Admin", "Inicio de Sesion");
        }else{
            Grafo RaizGrafos=EDDPY2_201701133.tb.BusquedaInicioSesion(usu, pas);
            if(RaizGrafos!=null){
                //Ingreso Correctamente
                Formulario_Usuario FormUsu=new Formulario_Usuario();
                FormUsu.RaizGrafo=RaizGrafos;
                FormUsu.FMUsuaio=FormUsu;
                FormUsu.Usuario=usu;
                FormUsu.show();
                this.dispose();
                                        //bitacora
                Calendar calendario = new GregorianCalendar();
                int hora =calendario.get(Calendar.HOUR_OF_DAY);
                int minutos = calendario.get(Calendar.MINUTE);
                int segundos = calendario.get(Calendar.SECOND);
                String tiempo=hora + ":" + minutos + ":" + segundos;
                EDDPY2_201701133.Bitacora.Insertar(tiempo, usu, "Inicio de Sesion");
                
            }else{
                JOptionPane.showMessageDialog(null, "--Error al Ingresar los datos--","INICIO SESION", JOptionPane.WARNING_MESSAGE);
            }
        }
        Limpiar();
        
        //creacion del la matriz de adyacencia
        
    }//GEN-LAST:event_jButton1ActionPerformed

